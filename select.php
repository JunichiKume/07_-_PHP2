<?php
//1.  DB接続します
try {
$pdo = new PDO('mysql:dbname=gs_db_2;charset=utf8;host=localhost','root','');
} catch (PDOException $e) {
  exit('データベースに接続できませんでした。'.$e->getMessage());
}

//２．データ表示SQL作成
$stmt = $pdo->prepare("SELECT * FROM gs_bm_table");
$status = $stmt->execute();

//３．データ表示

$view="";
if($status==false) {
    //execute（SQL実行時にエラーがある場合）
  $error = $stmt->errorInfo();
  exit("ErrorQuery:".$error[2]);
}else{
  //Selectデータの数だけ自動でループしてくれる
  //FETCH_ASSOC=http://php.net/manual/ja/pdostatement.fetch.php
  while( $result = $stmt->fetch(PDO::FETCH_ASSOC)){ 
    //$resultにデータが入ってくるのでそれを活用して[html]に表示させる為の変数を作成して代入する
    $view .= "<p>";
    $view .= $result["id"]." : ".$result["bookname"]." : ".$result["author"]." : ".$result["bookcategory"]." : ".$result["bookurl"]." : ".$result["finishdate"]." : ".$result["bookcomment"]." : ".$result["rating"]." : ".$result["indate"];
    $view .= "</p>";
  }
}

?>
